class Product {
  final String id;
  final String name;
  final String price;
  final String imageUrl;
  final String description;
  final int bedrooms;
  final int bathrooms;
  final int masterRooms;
  final int parkingSpaces;

  Product({
    required this.id,
    required this.name,
    required this.price,
    required this.imageUrl,
    required this.description,
    required this.bedrooms,
    required this.bathrooms,
    required this.masterRooms,
    required this.parkingSpaces,
  });
}
